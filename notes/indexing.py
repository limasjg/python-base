# With indexing you can access elements of a sequence using [] (indexing operator)
# [start: end : step]

cellphone = "41-985050-1065"

print(cellphone[0])
print(cellphone[:2])
print(cellphone[10:14])
print(cellphone[10:])
print(cellphone[-1])
print(cellphone[::2]) 
print(cellphone[-4:]) # last four numbers
print(cellphone[::-1]) # Inverse
